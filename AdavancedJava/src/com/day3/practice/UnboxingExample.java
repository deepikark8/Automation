package com.day3.practice;

public class UnboxingExample {
	public static void main(String[] args) {
		/*
		 * UnBoxing
		 * Converting Wrapper class to primitive data type
		 * 
		 * 
		 */
		Integer a1 = 200;
		System.out.println(a1);
		int a = a1;
		double a2 = (double)a1;
		System.out.println(a2);
		System.out.println(a);
		
		
	}

}
